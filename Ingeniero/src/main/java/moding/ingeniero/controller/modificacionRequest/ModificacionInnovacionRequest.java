package moding.ingeniero.controller.modificacionRequest;

public class ModificacionInnovacionRequest {

    private String nombreInnovacion;

    private String nuevoNombreInnovacion;

    private String nuevoLinkPaginaInnovacion;

    private String nuevaDescripcionInnovacion;

    private String nuevoCostoInnovacion;

    public String getNuevoLinkPaginaInnovacion() {
        return nuevoLinkPaginaInnovacion;
    }

    public void setNuevoLinkPaginaInnovacion(String nuevoLinkPaginaInnovacion) {
        this.nuevoLinkPaginaInnovacion = nuevoLinkPaginaInnovacion;
    }

    public String getNombreInnovacion() {
        return nombreInnovacion;
    }

    public void setNombreInnovacion(String nombreInnovacion) {
        this.nombreInnovacion = nombreInnovacion;
    }

    public String getNuevaDescripcionInnovacion() {
        return nuevaDescripcionInnovacion;
    }

    public void setNuevaDescripcionInnovacion(String nuevaDescripcionInnovacion) {
        this.nuevaDescripcionInnovacion = nuevaDescripcionInnovacion;
    }

    public String getNuevoCostoInnovacion() {
        return nuevoCostoInnovacion;
    }

    public void setNuevoCostoInnovacion(String nuevoCostoInnovacion) {
        this.nuevoCostoInnovacion = nuevoCostoInnovacion;
    }

    public String getNuevoNombreInnovacion() {
        return nuevoNombreInnovacion;
    }

    public void setNuevoNombreInnovacion(String nuevoNombreInnovacion) {
        this.nuevoNombreInnovacion = nuevoNombreInnovacion;
    }
}
